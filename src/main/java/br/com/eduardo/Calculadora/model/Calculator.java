package br.com.eduardo.Calculadora.model;

public class Calculator {

    public static float sum(Float n1, float n2){
        return n1 + n2;
    }

    public static float minus(Float n1, float n2){
        return n1 - n2;
    }

    public static float mult(Float n1, float n2){
        return n1 * n2;
    }

    public static float div(Float n1, float n2){
        return n1 / n2;
    }
}
